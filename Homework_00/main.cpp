#include <iostream>
using namespace std;
int main()
{
    for(int i = 0; i < 5; i++)
    {
        switch(i)
      {
        case 0:
            cout << "1 Apple" << endl;
            break;

        case 1:
            cout << "2 Oranges" << endl;
            break;

        case 2:
            cout << "3 Grapes" << endl;
            break;

        case 3:
            cout << "4 Pears" << endl;
            break;

        case 4:
            cout << "5 Bananas" << endl;
            break;
      }
    }



    return 0;
}
