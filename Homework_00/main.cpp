#include <iostream>
using namespace std;
int main()
{
    for(int i = 0; i < 5; i++)
    {
        switch(i)
      {
        case 0:
            cout << "Apple" << endl;
            break;

        case 1:
            cout << "Orange" << endl;
            break;

        case 2:
            cout << "Grape" << endl;
            break;

        case 3:
            cout << "Pear" << endl;
            break;

        case 4:
            cout << "Banana" << endl;
            break;
      }
    }



    return 0;
}
